    private void tableVarsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableVarsMouseClicked
        
        deleteRows(tableVarValues);
        int row = tableVars.getSelectedRow();
        int id = (int) tableVars.getValueAt(row, 0) ;
       
        for(int i=0; i < vars.size() ; i++)
        {
            if (id == vars.get(i).getId() )
            {
                showVarValues( vars.get(i).getValues());
                break;
            }
        }
    }//GEN-LAST:event_tableVarsMouseClicked

