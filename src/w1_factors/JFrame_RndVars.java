    private ArrayList<String> GetVarValues()
    {
        ArrayList<String> a = new ArrayList<String>();
        
        for(int r = 0; r < tableVarValues.getRowCount(); r++ )
        {
            a.add( tableVarValues.getValueAt(r, 1).toString() ) ;
        }    
        return a;
    }
    
