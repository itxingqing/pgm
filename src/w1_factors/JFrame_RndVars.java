    public void iniVars(){
        vars.clear();
        
        RandomVar v1 = new RandomVar(1, "v1", "Difficulty"  , new String[] { "easy", "hard"              });
        RandomVar v2 = new RandomVar(2, "v2", "Intelligence", new String[] { "low", "brilliant"          });
        RandomVar v3 = new RandomVar(3, "v3", "Grade"       , new String[] { "A", "B", "C"               });
        RandomVar v4 = new RandomVar(4, "v4", "Sat"         , new String[] { "excellent", "good", "bad"  });
        RandomVar v5 = new RandomVar(5, "v5", "Letter"      , new String[] { "recommendation", "ignored" });
        vars.add( v1 );
        vars.add( v2 );
        vars.add( v3 );
        vars.add( v4 );
        vars.add( v5 );
    }
    
    public void showVars(){
        
        //tableVars.getce
        for(int r = 0; r < vars.size(); r++ )
        {
            System.out.println( vars.get(r).getName());
            
            tableVars.setValueAt(vars.get(r).getId(),          r, 0);
            tableVars.setValueAt(vars.get(r).getName(),        r, 1);
            tableVars.setValueAt(vars.get(r).getDescription(), r, 2);
            tableVars.setValueAt(vars.get(r).getCardinality(), r, 3);
        }
    }
            
    protected void showVarValues(ArrayList<String> values){ 

        deleteRows(tableVarValues);
        
        for(int i=0; i < values.size(); i++)
        {
            varValueInsertRow( Integer.toString(i), values.get(i) );
        }
    }


    protected void deleteRows(JTable table){
        DefaultTableModel dm = (DefaultTableModel) table.getModel();
        int rowCount=dm.getRowCount();
        for (int i = 0; i < rowCount; i++) {
            dm.removeRow(0);
        }    
    }

    protected void varValueInsertRow(String id, String value ){
        DefaultTableModel dm = (DefaultTableModel) tableVarValues.getModel();
        dm.addRow(new Object[] { id, value} );
    }
            
    
