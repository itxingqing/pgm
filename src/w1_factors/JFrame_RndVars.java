    private void tableVarsKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tableVarsKeyReleased
        
        if ( evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            if ( addVar )
            {
                int    r    =          tableVars.getSelectedRow();
                int    id   = (int)    tableVars.getValueAt( r, 0);
                String name = (String) tableVars.getValueAt( r, 1);
                String desc = (String) tableVars.getValueAt( r, 2);
                
                RandomVar v = new RandomVar( id, name, desc );
                vars.add(v);
                addVar = false;
            }
        }
    }//GEN-LAST:event_tableVarsKeyReleased

